package com.stockmarket.csv;

public class BseEventsPojo {
	
	public String code;
	public String nseScriptCode;
	public String industry;
	public String purpose;
	public String meetingDate;
	public String announceDate;
	
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getNseScriptCode() {
		return nseScriptCode;
	}
	public void setNseScriptCode(String nseScriptCode) {
		this.nseScriptCode = nseScriptCode;
	}
	public String getIndustry() {
		return industry;
	}
	public void setIndustry(String industry) {
		this.industry = industry;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getMeetingDate() {
		return meetingDate;
	}
	public void setMeetingDate(String meetingDate) {
		this.meetingDate = meetingDate;
	}
	public String getAnnounceDate() {
		return announceDate;
	}
	public void setAnnounceDate(String announceDate) {
		this.announceDate = announceDate;
	}
	
	

}
